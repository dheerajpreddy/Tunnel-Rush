function tunnelConstructor(n) {
  obj = {};
  obj.num = n;
  obj.positions = [];
  for (var i = 0; i < obj.num; i++) {
    obj.positions.push(
      // Top face1
       -1.0,  1.0, -1.0- 2*i,
        0.0,  1.4, -1.0- 2*i,
        0.0,  1.4,  1.0- 2*i,
       -1.0,  1.0,  1.0- 2*i,

       // Top face2
        0.0,  1.4, -1.0-2*i,
        1.0,  1.0, -1.0-2*i,
        1.0,  1.0,  1.0-2*i,
        0.0,  1.4,  1.0-2*i,

       // Bottom face1
       -1.0, -1.0, -1.0-2*i,
        0.0, -1.4, -1.0-2*i,
        0.0, -1.4,  1.0-2*i,
       -1.0, -1.0,  1.0-2*i,

       // Bottom face2
        0.0, -1.4, -1.0-2*i,
        1.0, -1.0, -1.0-2*i,
        1.0, -1.0,  1.0-2*i,
        0.0, -1.4,  1.0-2*i,

       // R2*ight face1
        1.4,  0.0, -1.0-2*i,
        1.0,  1.0, -1.0-2*i,
        1.0,  1.0,  1.0-2*i,
        1.4,  0.0,  1.0-2*i,

       // R2*ight face2
        1.0, -1.0, -1.0-2*i,
        1.4,  0.0, -1.0-2*i,
        1.4,  0.0,  1.0-2*i,
        1.0, -1.0,  1.0-2*i,

       // Left face1
       -1.4,  0.0, -1.0-2*i,
       -1.4,  0.0,  1.0-2*i,
       -1.0,  1.0,  1.0-2*i,
       -1.0,  1.0, -1.0-2*i,

       // Left face2
       -1.0, -1.0, -1.0-2*i,
       -1.0, -1.0,  1.0-2*i,
       -1.4,  0.0,  1.0-2*i,
       -1.4,  0.0, -1.0-2*i,
    )
  }

  obj.indices = [];
  for (var i = 0; i < obj.num; i++) {
    for(var j = 0; j < 8; j++ )
    {
      obj.indices.push(32*i + 4*j, 32*i + 4*j + 1, 32*i + 4*j + 2, 32*i + 4*j, 32*i + 4*j + 2, 32*i + 4*j + 3);
    }
  }

  obj.textureCoordinates = [];
  for(var i=0; i<obj.num; i++) {
     obj.textureCoordinates.push(
      // Front
      0.0,  0.0,
      1.0,  0.0,
      1.0,  1.0,
      0.0,  1.0,
      // Back
      0.0,  0.0,
      1.0,  0.0,
      1.0,  1.0,
      0.0,  1.0,
      // Top
      0.0,  0.0,
      1.0,  0.0,
      1.0,  1.0,
      0.0,  1.0,
      // Bottom
      0.0,  0.0,
      1.0,  0.0,
      1.0,  1.0,
      0.0,  1.0,
      // Right
      0.0,  0.0,
      1.0,  0.0,
      1.0,  1.0,
      0.0,  1.0,
      // Left
      0.0,  0.0,
      1.0,  0.0,
      1.0,  1.0,
      0.0,  1.0,
      // l2
      0.0,  0.0,
      1.0,  0.0,
      1.0,  1.0,
      0.0,  1.0,
      // l3
      0.0,  0.0,
      1.0,  0.0,
      1.0,  1.0,
      0.0,  1.0,
    );
  }
  return obj;
}
